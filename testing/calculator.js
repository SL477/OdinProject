/**
 * add numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function add(a, b) {
    return a + b;
}

/**
 * subtract numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function subtract(a, b) {
    return a - b;
}

/**
 * divide numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function divide(a, b) {
    return a / b;
}

/**
 * multiply numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function multiply(a, b) {
    return a * b;
}
