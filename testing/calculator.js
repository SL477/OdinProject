/**
 * add numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function add(a, b) {
    return a + b;
}

/**
 * subtract numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function subtract(a, b) {
    return a - b;
}

/**
 * divide numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function divide(a, b) {
    return a / b;
}

/**
 * multiply numbers
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function multiply(a, b) {
    return a * b;
}

module.exports = {
    add,
    subtract,
    divide,
    multiply,
};
